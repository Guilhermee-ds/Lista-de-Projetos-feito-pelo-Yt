# Faça um Programa que leia 4 notas, mostre as notas e a média na tela.

a = float(input('mostre a primeira nota :'))
b = float(input('mostre a segunda nota:'))
c = float(input('mostre a terceira nota:'))
d = float(input('mostre a quarta nota :'))
media =(a + b + c +  d)/4
print('media: {}'.format(media))